package com.web.projectplanner.a04_vo;

public class Budget { //프로젝트 예산 목록
	private int budid;
	private long projectid; // 프로젝트 아이디 
	    private String projectname; // 프로젝트명
	    private String empno; // 사용자
	    private String budcontent; // 예산 항목 
	    
	    private int budget; // 예상비용
	    private int budActual; // 실제비용
	    private String status; // 사용 상태  
	    private String approval; // 승인 상태
		public int getBudid() {
			return budid;
		}
		public void setBudid(int budid) {
			this.budid = budid;
		}
		
		
		public long getProjectid() {
			return projectid;
		}
		public void setProjectid(long projectid) {
			this.projectid = projectid;
		}
		public String getProjectname() {
			return projectname;
		}
		public void setProjectname(String projectname) {
			this.projectname = projectname;
		}
		public String getEmpno() {
			return empno;
		}
		public void setEmpno(String empno) {
			this.empno = empno;
		}
		public String getBudcontent() {
			return budcontent;
		}
		public void setBudcontent(String budcontent) {
			this.budcontent = budcontent;
		}
		public int getBudget() {
			return budget;
		}
		public void setBudget(int budget) {
			this.budget = budget;
		}
		public int getBudActual() {
			return budActual;
		}
		public void setBudActual(int budActual) {
			this.budActual = budActual;
		}
		public String getStatus() {
			return status;
		}
		public void setStatus(String status) {
			this.status = status;
		}
		public String getApproval() {
			return approval;
		}
		public void setApproval(String approval) {
			this.approval = approval;
		}
		public Budget(int budid, long projectid, String projectname, String empno, String budcontent, int budget,
				int budActual, String status, String approval) {
			super();
			this.budid = budid;
			this.projectid = projectid;
			this.projectname = projectname;
			this.empno = empno;
			this.budcontent = budcontent;
			this.budget = budget;
			this.budActual = budActual;
			this.status = status;
			this.approval = approval;
		}
		public Budget() {
			super();
			// TODO Auto-generated constructor stub
		}
		

}